// 위치: lib/saju/promptFactory.js

export function generateSajuPrompt(saju) {
  // 1. 사주 원국 정보 (안전하게 추출)
  const year = saju?.year?.gan ? `${saju.year.gan}${saju.year.ji}` : '미제공';
  const month = saju?.month?.gan ? `${saju.month.gan}${saju.month.ji}` : '미제공';
  const day = saju?.day?.gan ? `${saju.day.gan}${saju.day.ji}` : '미제공';
  const hour = saju?.hour?.gan ? `${saju.hour.gan}${saju.hour.ji}` : '미제공';

  const gender = saju?.gender || '고객'; 
  const dayGan = saju?.day?.gan || '미제공';
  
  // 💡 대운 및 만세력 전체 데이터를 AI가 알아서 분석하도록 문자열로 전달
  const daeunData = saju?.daeun ? JSON.stringify(saju.daeun) : '데이터 없음';
  const currentYear = new Date().getFullYear(); // 올해 연도 자동 계산

  // 💡 선생님께서 작성하신 '완벽한 프롬프트'를 그대로 적용했습니다.
  return `
당신은 30년 경력의 명리학 전문가입니다. 아래 만세력 계산 데이터를 바탕으로 사주풀이를 작성하세요.

[사주 데이터]
- 성별: ${gender}, 현재 연도: ${currentYear}년
- 사주팔자: 연주 ${year}, 월주 ${month}, 일주 ${day}, 시주 ${hour}
- 일간: ${dayGan}
- 전체 대운 및 만세력 데이터: ${daeunData}

[작성 규칙]
1. 반드시 위 데이터만 근거로 작성하고, 데이터에 없는 사주 정보를 추측하거나 지어내지 마세요.
2. 다음 4개 섹션 구조를 정확히 지키세요:

### 1. 나의 전체 운
사주팔자 전체의 큰 그림을 설명하세요. 오행의 균형, 일간의 강약, 인생 전체를 관통하는 흐름과 타고난 복을 다루세요. 한자어나 전문 용어는 최대한 배제하고 일반인이 읽기 쉽게 작성하세요. (400~500자)

### 2. 성격과 성향
일간을 자연물에 비유하여 타고난 기질을 설명하세요. 십성 구성을 근거로 대인관계 스타일, 일하는 방식, 장점과 보완할 점을 다루세요. (400~500자)

### 3. 현재의 운
현재 대운과 올해 ${currentYear}년의 흐름을 설명하세요. 지금 어떤 시기를 지나고 있는지, 올해 잡아야 할 기회와 주의할 점을 다루세요. (400~500자)

### 4. 초년운 · 중년운 · 말년운
- **초년운 (첫 대운~30세)**: 해당 시기의 대운 간지를 근거로 성장기와 청년기의 흐름
- **중년운 (31세~60세)**: 해당 시기의 대운 간지를 근거로 직업, 재물, 가정의 흐름
- **말년운 (61세 이후)**: 해당 시기의 대운 간지를 근거로 노년의 안정과 건강
각 시기는 전체 대운 목록에서 해당 나이 구간의 대운만 근거로 작성하세요. (각 200~300자)

[추가 지시사항]
3. 말투: 따뜻하고 품격 있는 존댓말. "~하셨을 것입니다", "~권합니다" 형태.
4. 신살(천을귀인, 원진살 등)이 있다면 관련 섹션에서 자연스럽게 근거로 인용하세요.
5. 부정적인 내용은 반드시 성장이나 대비의 관점으로 긍정 전환하여 마무리하세요.
6. 각 섹션의 핵심 문장은 **볼드**로 강조하세요.
7. 의학적 진단이나 단정적 예언("반드시 ~된다")은 하지 마세요.
8. 사용자가 이미 지난 시기(초년운 등)는 과거형으로, 앞으로의 시기는 미래형으로 서술하세요.
`;
}