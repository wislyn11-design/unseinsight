// 위치: lib/saju/promptFactory.js

export function generateSajuPrompt(saju) {
  // 1. 사주 원국 정보 추출
  const year = saju?.year?.gan ? `${saju.year.gan}${saju.year.ji}` : '미제공';
  const month = saju?.month?.gan ? `${saju.month.gan}${saju.month.ji}` : '미제공';
  const day = saju?.day?.gan ? `${saju.day.gan}${saju.day.ji}` : '미제공';
  const hour = saju?.hour?.gan ? `${saju.hour.gan}${saju.hour.ji}` : '미제공';

  const gender = saju?.gender || '고객';
  
  // 전체 대운 데이터 및 현재 연도
  const daeunData = saju?.daeun ? JSON.stringify(saju.daeun) : '데이터 없음';
  const currentYear = new Date().getFullYear();

  return `
[시스템 가이드: 운세인사이트]
1. 본 분석은 '운세인사이트'의 정밀한 로직으로 산출된 데이터를 바탕으로 합니다.
2. 제공된 사주 정보는 검증된 값이므로 다시 계산하지 말고, 이 데이터를 절대적 기준으로 해석하십시오.
3. 답변 시작 시 '운세인사이트' 앱의 데이터를 바탕으로 해석함을 가볍게 언급하며, 명리학 전문가의 품격에 맞는 따뜻한 존댓말로 답변해 주십시오.

[사주 정보]
- 성별 : ${gender}
- 현재 연도 : ${currentYear}년
- 사주팔자 : 년주(${year}), 월주(${month}), 일주(${day}), 시주(${hour})

[대운 정보]
- 전체 대운 데이터 : ${daeunData}
(※ 위 전체 대운 데이터와 현재 연도(${currentYear}년)를 비교하여, 사용자가 '현재 지나고 있는 대운'의 시작 나이, 간지, 십성, 12운성을 정확히 파악한 뒤 아래 질문에 답하십시오.)

[질문 사항]
위 데이터를 바탕으로 명리학 전문가의 관점에서 다음 사항을 상세히 분석해 주십시오.

[출력 구조 및 작성 가이드]
- 프론트엔드 UI 화면 출력을 위해 반드시 아래 5개 항목의 제목 앞에 '### ' 기호를 붙여주십시오.
- 각 섹션의 핵심 문장은 **Bold** 처리를 하여 강조해 주십시오.
- 💡 (중요) 가독성을 위해 내용이 전환될 때 단락을 자주 나누고, 특히 '첫째, 둘째' 등의 항목이나 조언을 나열할 때는 **반드시 줄바꿈(Enter)**을 하여 시각적으로 읽기 편하게 작성해 주십시오.

### 1. 대운의 흐름과 환경 변화
현재 지나고 있는 '대운'의 간지와 십성 정보를 바탕으로, 이 시기가 사주 원국에 가져오는 전반적인 운의 흐름과 환경 변화를 분석해 주십시오.

### 2. 사회적 성취와 심리적 변화
현재 대운의 십성(천간/지지)과 12운성 수치를 절대적 근거로 삼아, 이 시기에 나타날 사회적 성취 가능성과 심리적 변화를 심층 설명해 주십시오.

### 3. 개인적 삶의 영역
이 대운 기간 동안의 직업 및 재물운, 그리고 건강과 대인관계를 포함한 개인적 삶의 영역에서 예상되는 주요 변화를 분석해 주십시오.

### 4. 전문가의 조언
명리학 전문가의 관점에서 이 시기에 반드시 잡아야 할 기회와, 특별히 주의하거나 보완해야 할 점을 구체적으로 조언해 주십시오.

### 5. 마음가짐과 현실적 행동 지침
본 대운이 다음 대운으로 넘어가는 과정에서 이 사주가 가져야 할 마음가짐과 현실적인 행동 지침을 들려주십시오.
`;
}