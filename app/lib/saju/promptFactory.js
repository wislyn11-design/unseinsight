export function generateSajuPrompt(saju) {
  // 1. 사주 원국 정보 추출
  const year = saju?.year?.gan ? `${saju.year.gan}${saju.year.ji}` : '미제공';
  const month = saju?.month?.gan ? `${saju.month.gan}${saju.month.ji}` : '미제공';
  const day = saju?.day?.gan ? `${saju.day.gan}${saju.day.ji}` : '미제공';
  const hour = saju?.hour?.gan ? `${saju.hour.gan}${saju.hour.ji}` : '미제공';

  const gender = saju?.gender || '고객';
  
  // 💡 [핵심 수정 포인트] 현재 연도와 월을 컴퓨터 시간 기준으로 실시간 계산합니다!
  const now = new Date();
  const currentYear = now.getFullYear();
  const currentMonth = now.getMonth() + 1; // 자바스크립트는 0월부터 시작하므로 +1을 해줍니다.

  const birthYear = saju?.solarDate ? parseInt(saju.solarDate.split('-')[0], 10) : currentYear;
  const currentAge = currentYear - birthYear + 1; 

  // 전체 대운 데이터
  const daeunData = saju?.daeun ? JSON.stringify(saju.daeun) : '데이터 없음';

  return `
[시스템 가이드: 운세인사이트]
1. 본 분석은 '운세인사이트'의 정밀한 로직으로 산출된 데이터를 바탕으로 합니다.
2. 제공된 사주 정보는 검증된 값이므로 다시 계산하지 말고, 이 데이터를 절대적 기준으로 해석하십시오.
3. 답변 시작 시 '운세인사이트' 앱의 데이터를 바탕으로 해석함을 가볍게 언급하며, 명리학 전문가의 품격에 맞는 따뜻한 존댓말로 답변해 주십시오.

[사주 정보]
- 성별 : ${gender}
- 출생 연도 : ${birthYear}년
- 현재 연도 : ${currentYear}년
- 현재 나이 : ${currentAge}세
- 사주팔자 : 년주(${year}), 월주(${month}), 일주(${day}), 시주(${hour})

[대운 정보]
- 전체 대운 데이터 : ${daeunData}
(※ 중요 지시사항: 위 전체 대운 데이터 배열에서 고객의 '현재 나이(${currentAge}세)'가 포함된 대운 구간을 정확히 찾으십시오. 엉뚱한 대운을 해석하지 말고, 반드시 이 현재 대운의 간지와 십성을 기준으로 아래 질문에 답해야 합니다.)

[질문 사항]
위 데이터를 바탕으로 명리학 전문가의 관점에서 다음 사항을 상세히 분석해 주십시오.

[출력 구조 및 작성 가이드]
- 프론트엔드 UI 화면 출력을 위해 반드시 아래 5개 항목의 제목 앞에 '### ' 기호를 붙여주십시오.
- 각 섹션의 핵심 문장은 **Bold** 처리를 하여 강조해 주십시오.
- 💡 (중요) 가독성을 위해 내용이 전환될 때 단락을 자주 나누고, 특히 '첫째, 둘째' 등의 항목이나 조언을 나열할 때는 **반드시 줄바꿈(Enter)**을 하여 시각적으로 읽기 편하게 작성해 주십시오.

[도입부: 💫 사주 총평 (타고난 기운)]
- 가장 먼저, 사용자의 사주 원국(태어난 연/월/일/시 8글자)과 오행의 분포를 바탕으로 이 사람이 타고난 전반적인 기질, 성향, 그리고 삶의 큰 테마를 2~3문단으로 깊이 있게 총평해 주세요.
- "고객님의 사주는 ~한 기운을 품고 태어나셨습니다..." 와 같이 따뜻하면서도 전문적인 톤으로 시작하세요.

### 대운
현재 지나고 있는 '대운'의 간지와 십성 정보를 바탕으로, 이 시기가 사주 원국에 가져오는 전반적인 운의 흐름과 환경 변화를 분석해 주십시오.


[출력 구조 및 작성 가이드]
### ${currentYear}년 전체 운세 
- 올해 사주 원국에 들어오는 기운(${currentYear}년 간지)을 바탕으로, 올해의 전반적인 테마와 흐름을 2~3문단으로 요약해 주십시오.

### 올해 반드시 명심해야 할 것
- 가장 조심해야 할 사항(건강, 재물 손실, 대인관계 갈등 등)과 이를 지혜롭게 피해 갈 방법을 현실적으로 조언해 주십시오.
- 올해 적극적으로 추진하면 좋을 '추천 행동'과 긍정적인 기회를 제시해 주십시오.

### ${currentYear}년 월별 핵심 운세
- 1월부터 12월까지 각 달의 핵심 운세를 한두 줄로 간결하고 명확하게 요약해 주십시오. (예: "3월: 새로운 인연이 찾아오는 시기. 모임에 적극적으로 참여하세요.")

### 올해의 Best 달 & 주의해야 할 달
- 1월~12월 중 가장 운기가 좋은 '최고의 달' 하나와, 가장 몸을 사리고 조심해야 할 '주의 달' 하나를 꼽아주고 그 이유를 간략히 설명해 주십시오.

[출력 구조 및 작성 가이드]
###  ${currentMonth}월 전체 운세
- 이번 달의 기운이 사주에 미치는 영향을 바탕으로 전반적인 운의 흐름을 짚어주십시오.

###  이번 달의 핵심 조언
- 이번 달을 무탈하고 평안하게 보내기 위해 꼭 지켜야 할 마음가짐이나 실천 지침을 하나만 강력하게 제안해 주십시오.


[출력 구조 및 작성 가이드]
###오늘의 운세 
- 오늘 하루의 전반적인 분위기와 컨디션을 긍정적이고 따뜻한 톤으로 3문장 이내로 요약해 주십시오.

###오늘의 행운 포인트
- 오늘 하루 나의 기운을 북돋워 줄 '행운의 컬러', '행운의 숫자', '행운의 방향'을 구체적으로 제시해 주십시오.

###시간대별 흐름 (오전/오후)
- **오전:** 오전 시간대의 기운과 추천하는 행동 방식을 짧게 적어주십시오.
- **오후/저녁:** 오후의 기운과 하루를 마무리하는 데 필요한 조언을 적어주십시오.

`;
}